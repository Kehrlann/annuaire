$(function(){
  $('#js-login-popover').popover({
      html : true,
      placement: "bottom",
      content: function() {
        return $("#js-login-form-content").html();
      }
  });

  $('.navbar').on('submit', '#js-login-form', function(e){
    // prevent the form from posting
    e.preventDefault();

    var form = $(e.target);
    var loginUrl = form.attr('action');

    var $this = $(e.target);

    $.ajax({
      type: "POST",
      url: loginUrl,
      data: form.serialize(),
      success: function(data, response){
        console.log("Success : "+data, response);
        window.location.replace(data);
      },
      error: function(){
        var message = "<div class='alert alert-danger'><button type='button' class='close' data-dismiss='alert'>&times;</button>Merci de bien vouloir r√©essayer.</div>";
        $this.find('.error').html(message);
      }
    });


  });

});






